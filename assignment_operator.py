""" assignment operator"""
a=5
print(a)
a+=5
print("after  using "" + ""assignment :",a)

b=5
print(b)
b-=5
print("after  using"' - '"assignment :",b)

c=5
print(c)
c*=5
print("after  using "" * "" assignment :",c)

d=5
print(d)
d/=5
print("after  using + assignment :",d)

e=5
print(e)
e%=5
print("after  using + assignment :",e)

f=5
print(f)
f//=5
print("after  using + assignment :",f)

g=5
print(g)
g**=5
print("after  using + assignment :",g)