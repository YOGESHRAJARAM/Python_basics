""" this is the example for arithmatic operator"""
s=4
k=2
print(s+k) # this is an addition operator 
print(s-k) # this is an subraction operator 
print(s*k) # this is an multiplication operator 
print(s/k) # this is an division operator 
print(s%k) # this is an modulus operator eg just print remainder if 4/2 0 is the remainder
print(s//k) # this is an floor division operator eg just round up the answers ( 4.1 =4)
print(s**k) # this is an exponentiation operator  eg 4 to the power of 4 (4*4*4*4)